Another method that is often used to get a rapid overview of your data is 
the function str(). The function str() shows you the structure of your data set.
For a data frame it tells you:

The total number of observations (e.g. 32 car types)
The total number of variables (e.g. 11 car features)
A full list of the variables names (e.g. mpg, cyl â€¦ )
The data type of each variable (e.g. num)
The first observations
Applying the str() function will often be the first thing that you do when 
receiving a new data set or data frame. It is a great way to get more insight 
in your data set before diving into the real analysis.

